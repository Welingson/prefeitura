        <div class="my-5">
            <p>O Coral “Vozes de Euterpe”, fundado em 12 de maio de 1962, é um Patrimônio Imaterial Tombado que, há 55
                anos, tornou-se o cartão de visita da cultura brazopolense, tendo se originado de um coro fundado em
                dezembro de 1960. Sua primeira apresentação foi realizada na participação das festividades da formatura
                da 4ª série ginasial do extinto Ginásio Brazópolis, naquele mesmo ano.
            </p>
            <p>A entidade, que nascia, seria uma sociedade para difusão da mais bela das artes: a música. A escolha do
                nome foi feita em votação direta, ganhando a sugestão do maestro Waldemar Pereira de Mendonça: Euterpe é
                a musa da música e da poesia lírica. Sr. José Rezende Vilela, dedicado coordenador e regente do Coral
                Vozes de Euterpe, há 55 anos, merece que nós, que nos encantamos com sua arte, reconheçamos a
                importância de seu trabalho. Seu repertório é constituído de músicas eruditas, sacras, litúrgicas,
                populares e folclóricas, com predominância das sacras e litúrgicas.
            </p>
