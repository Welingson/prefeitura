    <div class="my-5">
        <p>A bela igreja foi implantada em terrenos (pastos da Aparecida) de propriedade do Ten. Francisco José Dias
            Pereira, doados ao patrimônio paroquial por esse dinâmico cidadão, para o fim especial de nele
            edificar-se um templo sob a invocação da milagrosa Virgem. Firmino de Oliveira Melo foi o construtor da
            capela, cujas obras foram concluídas em 1862, recebendo, logo a seguir, a sagração mediante delegação do
            Bispado de São Paulo. Posteriormente, o Templo recebeu formas apreciáveis, com o objetivo de melhorar e
            ampliar suas instalações, tendo-se em vista o crescimento progressivo das atividades religiosas
            programadas para o culto a Nossa Senhora Aparecida e melhor abrigar o número crescente de peregrinos que
            ali comparecia anualmente. Ainda com o fim de atender às constantes romarias de fiéis, mais frequentes
            na ocasião do transcurso do Dia de Nossa Senhora, um novo projeto foi elaborado e executado, bem mais
            suntuoso, que também ampliou bastante a área anteriormente construída, transformando, assim, o templo na
            magnífica capela da atualidade. Concluída essa terceira e última reforma, o santuário foi inaugurado aos
            07 de abril de 1919, com seu belo altar-mor, artisticamente talhado em madeira escura, no estilo gótico,
            possuindo em seu teto uma pintura de rara beleza, obra do notável pintor itajubense, Luiz Teixeira,
            retratando a Assunção de Nossa Senhora, sendo uma cópia da tela de Bartolomé Esteban Murillo. Desde
            então, o Santuário de Nossa Senhora Aparecida passa a oferecer à Cidade Presépio a magnitude de suas
            linhas arquitetônicas, que muito contribuíram para enriquecer o contexto urbanístico local.
        </p>

