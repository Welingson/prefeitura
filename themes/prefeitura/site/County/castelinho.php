        <div class="my-5">
            <p>O Castelinho, como é popularmente denominado a edificação que abriga a Fundação Pedro Gomes Netto, surgiu
                do sonho de um homem, Doutor Carlos Piolli Filho, médico de profissão, nascido na cidade de Curitiba-PR,
                em 15 de setembro de 1908. Ele chegou à Brazópolis com o intuito de exercer a profissão no município. Ao
                final dos anos 40, Dr. Piolli encomendou ao arquiteto espanhol, José Garrido de San Martin, a construção
                de um castelo onde pudesse fixar residência. Junto com a construção do Castelinho cresceu também a
                curiosidade a respeito da edificação, afinal era bastante introspectivo e fechado, como todo castelo
                deveria ser. O castelo se tornou lenda para os moradores que fantasiavam histórias de loucuras e
                fantasmas. Tornou-se rapidamente notícia e cartão postal da cidade. A edificação foi doada, em vida, ao
                Asilo dos Inválidos Dona Maria Adelaide, entidade assistencial de apoio ao idoso e aos inválidos, na
                cidade de Brazópolis.
            </p>

