    <div class="my-5">
        <p>A história da Igreja matriz de São Caetano de Thiene tem início no ano de 1837, quando os moradores dos
            bairros de Cruz Vera,
            Anhumas e Ribeirão Abaixo se queixaram da distância da igreja que os servia,
            denominada Capela da Lage. A população então solicitou a construção de outra capela na Vargem Grande,
            designação
            dada ao povoado onde se localiza o município de Brazópolis.Por volta de 1835 , a família Dias Chaves,
            proprietária da Fazenda
            Várzea Grande, construiu uma capela, dedicada a Sant’Ana, no local onde hoje é a Praça dos Sagrados
            Corações, de Brazópolis. Mais tarde, em 1837, com a intenção de transformar a capela em Capela Curada,
            Ana Dias Chaves, coproprietária das terras onde estava a capela, juntamente com seu pai, Francisco Dias
            Chaves, doaram cerca de 30 alqueires para constituir o patrimônio da capela. A sua construção teve
            início em 1837, tendo sido finalizada apenas em 1884 com a colocação dos sinos nas torres. A primeira
            padroeira foi Nossa Senhora de Santana porém, como não havia uma imagem para homenageá-la, o coronel da
            época, de nome Caetano Ferreira da Costa, sogro do Coronel Francisco Braz sugeriu que,”... se colocassem
            como padroeiro da paróquia, São Caetano de Thiene, ele ofereceria a imagem à igreja.” E assim foi
            feito!</p>
        <p>Nove anos mais tarde, em 1847, a Capelinha tornou-se insuficiente para abrigar toda a população do
            povoado, que crescera demasiadamente na última década. Assim, tornou-se necessária a construção de uma
            nova igreja, bem maior que a originária, onde os moradores pudessem praticar a fé Católica. Finalmente,
            em três de maio de 1878, a construção da igreja é concluída e a inauguração aconteceu com grande festa e
            missa, contando com a participação de toda a comunidade.</p>
        <p>A Igreja Matriz de São Caetano de Thiene e Sant’Ana possui arquitetura de rara beleza, destacando-se na
            paisagem, podendo ser visualizada praticamente em todos os pontos da cidade. Vê-se ainda em seu
            interior, um pequeno museu em homenagem ao Monsenhor Quinzinho, onde estão expostos pertences e peças
            usadas por ele. Seu processo de beatificação encontra-se em estágio avançado.</p>
