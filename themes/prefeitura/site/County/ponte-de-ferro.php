        <div class="my-5">
            <p>A Ponte de Ferro foi construída em 1930 fazendo a ligação entre a Vila Braz e São José do Paraíso,
                passando pela Estação de Cruz Vera, também dentro do município de Brazópolis. As peças que constituem a
                Ponte de Ferro possuem origem britânica. Elas foram trazidas da Inglaterra até o Porto de Santos,
                litoral de São Paulo, de onde partiu até Brazópolis, em vagões da própria linha férrea. Esta obra
                facilitou o escoamento de toda a produção agrícola do município, principalmente a cafeeira.
                Paralelamente possibilitou a realização de viagens rápidas, seguras e confortáveis, uma vez que as
                rodovias eram bastante precárias. Durante mais de meio século, a Ponte de Ferro sobre o Rio Vargem
                Grande exerceu sua função principal, sendo que por ela passou grande parte das mercadorias
                comercializadas na região Sul de Minas Gerais. Devido ao declínio da cultura do café, a partir de 1930 e
                a Segunda Grande Guerra, vários ramais foram desativados, principalmente nas décadas de 1960 e 1970.
            </p>
