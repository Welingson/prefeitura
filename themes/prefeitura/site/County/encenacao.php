            <p>As encenações tiveram início no interior da Igreja Matriz, ainda nos moldes da encenação antiga, mas já
                com o arrojo que lhe é peculiar.</p>
                <p>Apenas a Sexta-feira Santa era então encenada, sendo acrescentados gradualmente o Domingo de Ramos, o
                Sábado da Aleluia e o Domingo da Ressurreição. À frente daquele elenco, revelou-se então o ator José
                Renato Noronha, no papel do primeiro Cristo desta série de ‘Paixões’ Pelos anos seguintes o elenco foi
                naturalmente transformando-se, ficando o árduo papel do “Cristo”, por um ano ou mais, sob a
                responsabilidade dos atores: Antonio Claret, José Luiz Júnior, Édison Manfredini e, nesta última versão,
                Robson Aparecido da Silva. Uma grande montagem a cada ano é feita, concorrendo para isso o valioso
                trabalho de grande número de pessoas entre atores, atrizes, costureiras, maquiadores, carpinteiros,
                músicos e cantores, técnicos em iluminação e filmagem, para compor o enorme palco. Encimando a tudo
                isto, a direção geral de Fátima Machado que, incansavelmente, consegue trazer sob seu absoluto controle
                    uma centena de pessoas, oferecendo a milhares outras, um espetáculo grandioso.</p>
                <p>É importante salientar que, para o Teatro Amador Brazopolense - TAB, tanto este trabalho como outros
                autos religiosos levados a público, têm por finalidade precípua o avivamento da fé, oferecendo também
                grande oportunidade de reflexão sobre problemas atuais, como as drogas e a exclusão social. Quanto ao
                seu elenco, é iniludível que se compõe, na grande maioria, de pessoas comuns do nosso meio, porém, com a
                boa vontade e a intuição, servindo-lhes de apoio técnico para subir ao palco e corporificar situações
                humanas.</p>
